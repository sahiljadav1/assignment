# Practical Example 2: How to create variables in Python? 

# Integer variable
age = 30  
print("Age:", age)  

# Float variable
height = 5.9  
print("Height:", height)  

# String variable
name = "Alice"  
print("Name:", name)  

# Boolean variable
is_student = True  
print("Is student:", is_student)  

# List variable 
fruits = ["apple", "banana", "cherry"]  
print("Fruits:", fruits)  
